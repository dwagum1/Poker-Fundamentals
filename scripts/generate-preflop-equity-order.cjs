/**
 * Monte Carlo preflop equity vs one random hand (random board).
 * Writes src/lib/poker/starting-hands/order-169-equity-data.ts
 *
 * Usage: node scripts/generate-preflop-equity-order.cjs
 */

const fs = require("fs");
const path = require("path");
const { Hand } = require("pokersolver");

const RANK_CHARS = "AKQJT98765432";
const SUITS = ["s", "h", "d", "c"];

const FULL_DECK = [];
for (const r of RANK_CHARS) {
  for (const s of SUITS) {
    FULL_DECK.push(r + s);
  }
}

/** Deterministic PRNG (Mulberry32) for reproducible output. */
function mulberry32(seed) {
  return function () {
    let t = (seed += 0x6d2b79f5);
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

function shuffleInPlace(arr, rng) {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(rng() * (i + 1));
    const t = arr[i];
    arr[i] = arr[j];
    arr[j] = t;
  }
}

function enumerateAllCodes() {
  const out = [];
  for (let i = 0; i < RANK_CHARS.length; i++) {
    out.push(RANK_CHARS[i] + RANK_CHARS[i]);
  }
  for (let i = 0; i < RANK_CHARS.length; i++) {
    for (let j = i + 1; j < RANK_CHARS.length; j++) {
      const hi = RANK_CHARS[i];
      const lo = RANK_CHARS[j];
      out.push(hi + lo + "s");
      out.push(hi + lo + "o");
    }
  }
  return out;
}

/** Fixed representative hole cards per canonical code (for equity vs random). */
function codeToHeroCards(code) {
  if (code.length === 2 && code[0] === code[1]) {
    const r = code[0];
    return [r + "s", r + "h"];
  }
  if (code.endsWith("s")) {
    const a = code[0];
    const b = code[1];
    return [a + "s", b + "s"];
  }
  if (code.endsWith("o")) {
    const a = code[0];
    const b = code[1];
    return [a + "s", b + "h"];
  }
  throw new Error("bad code: " + code);
}

const ITERATIONS = 28_000;

function equityVsRandom(hero, rng) {
  const pool = FULL_DECK.filter((c) => !hero.includes(c));
  let wins = 0;
  for (let n = 0; n < ITERATIONS; n++) {
    shuffleInPlace(pool, rng);
    const board = [pool[0], pool[1], pool[2], pool[3], pool[4]];
    const villain = [pool[5], pool[6]];
    const h1 = Hand.solve([...hero, ...board]);
    const h2 = Hand.solve([...villain, ...board]);
    const w = Hand.winners([h1, h2]);
    if (w.length === 2) wins += 0.5;
    else if (w[0] === h1) wins += 1;
  }
  return wins / ITERATIONS;
}

function main() {
  const codes = enumerateAllCodes();
  if (codes.length !== 169) {
    throw new Error("expected 169 codes, got " + codes.length);
  }

  const rng = mulberry32(0xdecafbad);
  const rows = [];

  for (const code of codes) {
    const hero = codeToHeroCards(code);
    const eq = equityVsRandom(hero, rng);
    rows.push({ code, eq });
    process.stdout.write(".");
  }
  console.log("");

  rows.sort((a, b) => {
    const d = b.eq - a.eq;
    if (Math.abs(d) > 1e-9) return d;
    return a.code.localeCompare(b.code);
  });

  const order169 = rows.map((r) => r.code);
  const equityMap = {};
  for (const r of rows) {
    equityMap[r.code] = Math.round(r.eq * 100_000) / 100_000;
  }

  const outPath = path.join(
    __dirname,
    "..",
    "src",
    "lib",
    "poker",
    "starting-hands",
    "order-169-equity-data.ts",
  );

  const fileBody = `/**
 * Auto-generated by scripts/generate-preflop-equity-order.cjs — do not edit by hand.
 * Monte Carlo equity vs one random opponent hand (${ITERATIONS.toLocaleString()} deals per combo, seeded RNG).
 */

export const ORDER_169 = ${JSON.stringify(order169)} as const;

/** Approximate P(win) + 0.5·P(tie) for all-in runout vs random villain hole cards. */
export const EQUITY_VS_RANDOM: Record<string, number> = ${JSON.stringify(equityMap, null, 2)};
`;

  fs.writeFileSync(outPath, fileBody, "utf8");
  console.log("Wrote", outPath);

  const i92o = order169.indexOf("92o");
  const i87s = order169.indexOf("87s");
  console.log("Check 92o vs 87s: index 92o=", i92o, "index 87s=", i87s, "87s stronger?", i87s < i92o);
}

main();
